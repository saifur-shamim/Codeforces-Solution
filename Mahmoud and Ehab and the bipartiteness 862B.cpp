bool dfs(int node,int c)
{
    vis[node]=1;
    col[node]=c;

    v[c].PB(node);

    for(int child: adj[node])
    {
        if(vis[child]==0)
        {
            if(dfs(child,c^1)==false)return false;
        }
        else
        {
            if(col[node]==col[child])return false;
        }
    }
    return true;
}


